public class EnfantWithInsc {
    private Integer Id;
    private String nom;
    private String prenom;
    private String dr_email;
    private String kindergarten_name;

    /**
     * @return Integer return the Id
     */
    public Integer getId() {
        return Id;
    }

    /**
     * @param Id the Id to set
     */
    public void setId(Integer Id) {
        this.Id = Id;
    }

    /**
     * @return String return the nom
     */
    public String getNom() {
        return nom;
    }

    /**
     * @param nom the nom to set
     */
    public void setNom(String nom) {
        this.nom = nom;
    }

    /**
     * @return String return the prenom
     */
    public String getPrenom() {
        return prenom;
    }

    /**
     * @param prenom the prenom to set
     */
    public void setPrenom(String prenom) {
        this.prenom = prenom;
    }

    /**
     * @return String return the dr_email
     */
    public String getDr_email() {
        return dr_email;
    }

    /**
     * @param dr_email the dr_email to set
     */
    public void setDr_email(String dr_email) {
        this.dr_email = dr_email;
    }

    /**
     * @return String return the kindergarten_name
     */
    public String getKindergarten_name() {
        return kindergarten_name;
    }

    /**
     * @param kindergarten_name the kindergarten_name to set
     */
    public void setKindergarten_name(String kindergarten_name) {
        this.kindergarten_name = kindergarten_name;
    }

}
