package com.kindergarten.kindergarten.parent;

public class InscriptionUI {
    private Integer id;
    private String date;
    private String anneescol;
    private String class_level;
    private Integer kgid;
    private Integer enfid;

    /**
     * @return Integer return the id
     */
    public Integer getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(Integer id) {
        this.id = id;
    }

    /**
     * @return String return the date
     */
    public String getDate() {
        return date;
    }

    /**
     * @param date the date to set
     */
    public void setDate(String date) {
        this.date = date;
    }

    /**
     * @return String return the anneescol
     */
    public String getAnneescol() {
        return anneescol;
    }

    /**
     * @param anneescol the anneescol to set
     */
    public void setAnneescol(String anneescol) {
        this.anneescol = anneescol;
    }

    /**
     * @return Integer return the kgid
     */
    public Integer getKgid() {
        return kgid;
    }

    /**
     * @param kgid the kgid to set
     */
    public void setKgid(Integer kgid) {
        this.kgid = kgid;
    }

    /**
     * @return Integer return the enfid
     */
    public Integer getEnfid() {
        return enfid;
    }

    /**
     * @param enfid the enfid to set
     */
    public void setEnfid(Integer enfid) {
        this.enfid = enfid;
    }

    /**
     * @return String return the class_level
     */
    public String getClass_level() {
        return class_level;
    }

    /**
     * @param class_level the class_level to set
     */
    public void setClass_level(String class_level) {
        this.class_level = class_level;
    }

}
